using System;

namespace ArithmeticOperations
{
    class ArithmeticOperations
    {
        static void F6()
        {
            int x = 10;
            int x1 = x + 3; // 13
            int x2 = x1 - 6; // 7
            int x3 = x2 * 10; // 70
            int x4 = x3 / 5; // 14 
            int x5 = x4 % 3; // 4 
            int x6 = x5++; // 5
            int x7 = x6--; // 4
            int x8 = x5+++x*x7; // 45
            int x9 = x / x1 * 20; // 15
        }
    }
}